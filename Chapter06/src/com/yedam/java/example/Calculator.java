package com.yedam.java.example;

public class Calculator {
		//더하기
		int plus(int x, int y) {
			int result = 0;
			result = x + y;
			
			return result;
		}
		
		//뺄셈
		int minus(int x, int y) {
			int result = 0;
			result = x - y;
			
			return result;
		}
		
		//곱셈
		int mul(int x, int y) {
			int result = 0;
			result = x * y;
			
			return result;
		}
		
		//나누기
		double devide(double x, double y) {
			double result = 0;
			result = x / y;
			
			return result;
		}
}
